    private void LecSaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LecSaveButtonActionPerformed
         if(LecID.getText().equals("") || LecName.getText().equals("")){
        JOptionPane.showMessageDialog(this, "Please Fill Empty fields ","Empty Field",JOptionPane.ERROR_MESSAGE);
      }else{
        if(LecID.getText().length()==5){
        Session session = HibernateUtil.getSessionFactory().openSession();
        Transaction tx = session.beginTransaction();
        try{
           Person lec = new Person();
           lec.setPId(LecID.getText());
           lec.setPName(LecName.getText());
           lec.setPType("Lecturer");
           session.save(lec);
           tx.commit();
           JOptionPane.showMessageDialog(this,"Successful Registered","DATA SAVED",JOptionPane.INFORMATION_MESSAGE);
            
        }catch(HibernateException ex){
            System.out.println(ex);
        }finally{
        session.close();
        }
        }else{
        JOptionPane.showMessageDialog(this,"You need a maximum of 5 Digits","Incorrect Lecturer ID",JOptionPane.ERROR_MESSAGE);
        }
            
        }
    }//GEN-LAST:event_LecSaveButtonActionPerformed

