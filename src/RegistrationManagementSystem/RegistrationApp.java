    private void StudSaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StudSaveButtonActionPerformed
        if(StudID.getText().equals("") || StudName.getText().equals("")){
        JOptionPane.showMessageDialog(this, "Please Fill Empty fields ","Empty Field",JOptionPane.ERROR_MESSAGE);
      }else{
        if(StudID.getText().length()==5){
            //calling database and save data
        Session session = HibernateUtil.getSessionFactory().openSession();
        Transaction tx = session.beginTransaction();
        try{
           Person student = new Person();
           student.setPId(StudID.getText());
           student.setPName(StudName.getText());
           student.setPType("Student");
           session.save(student);
           tx.commit();
           JOptionPane.showMessageDialog(this,"Successful Registered","DATA SAVED",JOptionPane.INFORMATION_MESSAGE);
           if(JOptionPane.OK_OPTION ==0){
               StudID.setText("");
               StudName.setText("");
               studentModel.removeAllElements();
               addItemInStudentList();
           }
            
        }catch(HibernateException ex){
            System.out.println(ex);
        }finally{
        session.close();
        }
        }else{
        JOptionPane.showMessageDialog(this,"You need a maximum of 5 Digits","Incorrect Student ID",JOptionPane.ERROR_MESSAGE);
        }
            
        }
    }//GEN-LAST:event_StudSaveButtonActionPerformed

    private void CrsSaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrsSaveButtonActionPerformed
         if(CrsID.getText().equals("") || CrsName.getText().equals("")){
        JOptionPane.showMessageDialog(this, "Please Fill Empty fields ","Empty Field",JOptionPane.ERROR_MESSAGE);
      }else{
        if(CrsID.getText().length()==5){
        Session session = HibernateUtil.getSessionFactory().openSession();
        Transaction tx = session.beginTransaction();
        try{
           Course crs = new Course();
           crs.setCId(CrsID.getText());
           crs.setCName(CrsName.getText());
           String selectedLect = CrsInstructor.getSelectedItem().toString();
           Person lect = new Person();
           lect.setPId(selectedLect.substring(0, 5));
           crs.setPerson(lect);
           session.save(crs);
           //UPDATE TABLE FOR PERSON
           String selectedStudent = StudList.getSelectedValue();
           Person updateStudent =(Person) session.get(Person.class, selectedStudent.substring(0, 5));
           updateStudent.setCourseId(CrsID.getText());
           session.update(updateStudent);
           tx.commit();
           JOptionPane.showMessageDialog(this,"Successful Registered","DATA SAVED",JOptionPane.INFORMATION_MESSAGE);
           if(JOptionPane.OK_OPTION ==0){
               CrsID.setText("");
               CrsName.setText("");
           }
            
        }catch(HibernateException ex){
            System.out.println(ex);
        }finally{
        session.close();
        }
        }else{
        JOptionPane.showMessageDialog(this,"You need a maximum of 5 Digits","Incorrect Course ID",JOptionPane.ERROR_MESSAGE);
        }
            
        }
    }//GEN-LAST:event_CrsSaveButtonActionPerformed

    
    private void addItemInLecturerCombo(){
    Session session = HibernateUtil.getSessionFactory().openSession();
    List lectList = session.createQuery("from Person p where p.PType='Lecturer'").list();
    Iterator iterator = lectList.iterator();
    while (iterator.hasNext()){
        Person lect =(Person)iterator.next();
        CrsInstructor.addItem(lect.getPId()+"-"+lect.getPName());
        
    }
    
    }
     private void addItemInStudentList(){
    Session session = HibernateUtil.getSessionFactory().openSession();
    List lectList = session.createQuery("from Person p where p.PType='Student'").list();
    Iterator iterator = lectList.iterator();
    while (iterator.hasNext()){
        Person stud =(Person)iterator.next();
        studentModel.addElement(stud.getPId()+"-"+stud.getPName());
        
    }
    StudList.setModel(studentModel);
    }
    
